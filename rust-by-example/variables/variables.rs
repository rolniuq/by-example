fn main() {
    let x = 5;
    println!("The value of x is: {}", x);

    let mut y = 5;
    println!("The value of y is: {}", y);

    y = 6;
    println!("The value of y is: {}", y);

    let z = 5; // i32
    println!("The value of z is: {}", z);

    let a: i32 = 5;
    println!("The value of a is: {}", a);

    let a = "hello, world!";
    println!("The value of a is: {}", a);
}
